package pe.com.petroperu.model;

public class Valija {

	private Long idValija;
	private String codCentroGestionRemitente;
	private String codCourier;
	private String identificadorValija;
	private String ordenServicio;
	private String rotuloEtiqueta;
	private String rotuloMuestra;
	public Long getIdValija() {
		return idValija;
	}
	public void setIdValija(Long idValija) {
		this.idValija = idValija;
	}
	public String getCodCentroGestionRemitente() {
		return codCentroGestionRemitente;
	}
	public void setCodCentroGestionRemitente(String codCentroGestionRemitente) {
		this.codCentroGestionRemitente = codCentroGestionRemitente;
	}
	public String getCodCourier() {
		return codCourier;
	}
	public void setCodCourier(String codCourier) {
		this.codCourier = codCourier;
	}
	public String getIdentificadorValija() {
		return identificadorValija;
	}
	public void setIdentificadorValija(String identificadorValija) {
		this.identificadorValija = identificadorValija;
	}
	public String getOrdenServicio() {
		return ordenServicio;
	}
	public void setOrdenServicio(String ordenServicio) {
		this.ordenServicio = ordenServicio;
	}
	public String getRotuloEtiqueta() {
		return rotuloEtiqueta;
	}
	public void setRotuloEtiqueta(String rotuloEtiqueta) {
		this.rotuloEtiqueta = rotuloEtiqueta;
	}
	public String getRotuloMuestra() {
		return rotuloMuestra;
	}
	public void setRotuloMuestra(String rotuloMuestra) {
		this.rotuloMuestra = rotuloMuestra;
	}
	
	
	
	
}
