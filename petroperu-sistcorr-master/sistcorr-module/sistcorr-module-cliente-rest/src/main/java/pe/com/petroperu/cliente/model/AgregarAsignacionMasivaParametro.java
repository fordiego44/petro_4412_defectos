package pe.com.petroperu.cliente.model;


public class AgregarAsignacionMasivaParametro
{
  private String codigoAccion;
  private String correlativo;
  private String detalleSolicitud;
  private String fechaLimite;
  private String idPadre;
  private Integer codigoDependencia;
  
  public String getCodigoAccion() { return this.codigoAccion; }

  
  public void setCodigoAccion(String codigoAccion) { this.codigoAccion = codigoAccion; }

  
  public String getCorrelativo() { return this.correlativo; }

  
  public void setCorrelativo(String correlativo) { this.correlativo = correlativo; }

  
  public String getDetalleSolicitud() { return this.detalleSolicitud; }

  
  public void setDetalleSolicitud(String detalleSolicitud) { this.detalleSolicitud = detalleSolicitud; }

  
  public String getFechaLimite() { return this.fechaLimite; }

  
  public void setFechaLimite(String fechaLimite) { this.fechaLimite = fechaLimite; }

  
  public String getIdPadre() { return this.idPadre; }

  
  public void setIdPadre(String idPadre) { this.idPadre = idPadre; }

  
  public Integer getCodigoDependencia() { return this.codigoDependencia; }

  
  public void setCodigoDependencia(Integer codigoDependencia) { this.codigoDependencia = codigoDependencia; }
}
