package pe.com.petroperu.cliente.model;

public class CerrarCorrespondencia
{
  private String correlativo;
  private String wobNum;
  private String observacion;
  
  public String getCorrelativo() { return this.correlativo; }

  
  public void setCorrelativo(String correlativo) { this.correlativo = correlativo; }

  
  public String getWobNum() { return this.wobNum; }

  
  public void setWobNum(String wobNum) { this.wobNum = wobNum; }

  
  public String getObservacion() { return this.observacion; }

  
  public void setObservacion(String observacion) { this.observacion = observacion; }
}
